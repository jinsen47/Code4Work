    public static boolean isDataValid(String source) {
        // 源串为空或过短, 不包含校验位
        if (source == null || source.length() < 3) return false;

        // 倒数第三位不是*, 没有校验位
        if (source.charAt(source.length() - 3) != '*') return false;

        String checkString = source.substring(source.length() - 2, source.length());
        int checkInt = Integer.parseInt(checkString, 16);

        String stringToCheck = source.substring(1, source.length() - 3);
        int sum = 0;
        for (int i = 0; i< stringToCheck.length(); i++) {
            sum ^= stringToCheck.charAt(i);
        }

        if (sum == checkInt) {
            return true;
        } else {
            return false;
        }
    }

